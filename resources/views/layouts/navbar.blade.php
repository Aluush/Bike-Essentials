    document.addEventListener('DOMContentLoaded', function() {
        // V√©rifiez les pr√©f√©rences utilisateur pour le mode sombre
        if (localStorage.getItem('theme') === 'dark' || (!localStorage.getItem('theme') && window.matchMedia(
                '(prefers-color-scheme: dark)').matches)) {
            document.documentElement.classList.add('dark');
            document.querySelectorAll('.theme-toggle-dark-icon').forEach(icon => icon.classList.remove(
                'hidden'));
        } else {
            document.documentElement.classList.remove('dark');
            document.querySelectorAll('.theme-toggle-light-icon').forEach(icon => icon.classList.remove(
                'hidden'));
        }

        var themeToggleBtns = document.querySelectorAll('.theme-toggle');

        themeToggleBtns.forEach(function(themeToggleBtn) {
            themeToggleBtn.addEventListener('click', function() {
                var darkIcons = document.querySelectorAll('.theme-toggle-dark-icon');
                var lightIcons = document.querySelectorAll('.theme-toggle-light-icon');

                darkIcons.forEach(icon => icon.classList.toggle('hidden'));
                lightIcons.forEach(icon => icon.classList.toggle('hidden'));

                if (document.documentElement.classList.contains('dark')) {
                    document.documentElement.classList.remove('dark');
                    localStorage.setItem('theme', 'light');
                } else {
                    document.documentElement.classList.add('dark');
                    localStorage.setItem('theme', 'dark');
                }
            });
        });
    });
</script>